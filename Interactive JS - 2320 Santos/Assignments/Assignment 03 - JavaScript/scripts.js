function Enlarge(li)
{
	li.className = "upsize";  
}

function Shrink(li)
{
	li.className = ""; 
}

function clickScript(button) 
{
	button.innerHTML = "Thank You!" ;
}

function Bold(navLi)
{
	navLi.className = "emphasize";
}

function Return(navLi) 
{
	navLi.className = "";
} 

function tableHighlight() 
{
	document.getElementById('table').style.border = "3px solid yellow"; 
}



